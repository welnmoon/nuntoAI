import type { Metadata } from "next";
import "./globals.css";
import { Toaster } from "react-hot-toast";
import { AccentColorApplier } from "@/components/accent-color-applier";
import { Inter } from "next/font/google";
import SessionClientProvider from "@/components/providers/session-client";
import { getServerSession } from "next-auth";
import { authOptions } from "@/lib/auth";
import { RootProvider } from "fumadocs-ui/provider";

const inter = Inter({
  subsets: ["latin", "cyrillic"],
  display: "swap",
  variable: "--font-geist-sans",
});

export const metadata: Metadata = {
  title: "Nunto AI",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const session = await getServerSession(authOptions);
  return (
    <html lang="ru">
      <body className={`${inter.variable} font-sans m-0 p-0`}>
        <RootProvider>
          <SessionClientProvider session={session}>
            <Toaster position="top-right" />
            <AccentColorApplier />
            {children}
          </SessionClientProvider>
        </RootProvider>
      </body>
    </html>
  );
}
